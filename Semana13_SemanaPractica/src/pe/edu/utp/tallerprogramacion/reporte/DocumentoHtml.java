package pe.edu.utp.tallerprogramacion.reporte;

/**
 *
 * @author Christiam Calero
 */
public class DocumentoHtml {
    private Head head;
    private Body body;

    public DocumentoHtml(Head head, Body body) {
        this.head = head;
        this.body = body;
    }

    public Head getHead() {
        return head;
    }

    public void setHead(Head head) {
        this.head = head;
    }

    public Body getBody() {
        return body;
    }

    public void setBody(Body body) {
        this.body = body;
    }

    @Override
    public String toString() {
        return "<html>" + head + body + "</html>";
    }
    
    
            
}
